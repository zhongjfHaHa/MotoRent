package cn.com.atbm.dome02;

public class Employee {
	private int age;
	private char sex;
	private String name;
	private String address;
	
	public Employee(){
		super();
	}
	
	public Employee(int age,char sex,String name,String address){
		super();
		this.age = age;
		this.sex = sex;
		this.name = name;
		this.address = address;	
	}
}
